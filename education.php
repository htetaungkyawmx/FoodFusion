<script>
document.addEventListener('DOMContentLoaded', function() {
    // Learning path buttons
    document.querySelectorAll('.path-card .btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const pathName = this.closest('.path-card').querySelector('h3').textContent;
            alert(`Starting "${pathName}" learning path...\n\n(In a real application, this would start the course)`);
        });
    });
    
    // Read article links
    document.querySelectorAll('.read-more').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const articleName = this.closest('.article-card').querySelector('h3').textContent;
            alert(`Opening article: "${articleName}"\n\n(In a real application, this would open the full article)`);
        });
    });
    
    // Animate plate sections on scroll
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const plateSections = entry.target.querySelectorAll('.plate-section');
                plateSections.forEach((section, index) => {
                    setTimeout(() => {
                        section.style.opacity = '1';
                        section.style.transform = 'scale(1)';
                    }, index * 200);
                });
            }
        });
    }, { threshold: 0.5 });
    
    const plateVisual = document.querySelector('.plate-visual');
    if (plateVisual) {
        observer.observe(plateVisual);
        // Set initial state
        document.querySelectorAll('.plate-section').forEach(section => {
            section.style.opacity = '0';
            section.style.transform = 'scale(0.8)';
            section.style.transition = 'all 0.5s ease';
        });
    }
});
</script>

