<script>
document.addEventListener('DOMContentLoaded', function() {
    // Animated counters for hero stats
    const statNumbers = document.querySelectorAll('.stat-number');
    statNumbers.forEach(stat => {
        const text = stat.textContent;
        const number = parseInt(text.replace(/[^0-9]/g, ''));
        const suffix = text.replace(number.toString(), '');
        
        stat.textContent = '0' + suffix;
        
        if (number > 0) {
            let count = 0;
            const duration = 2000;
            const increment = number / (duration / 16);
            
            const counter = setInterval(() => {
                count += increment;
                if (count >= number) {
                    count = number;
                    clearInterval(counter);
                }
                stat.textContent = Math.round(count).toLocaleString() + suffix;
            }, 16);
        }
    });
    
    // Intersection Observer for fade-in animations
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                
                // Animate timeline items sequentially
                if (entry.target.classList.contains('timeline-item')) {
                    const items = document.querySelectorAll('.timeline-item');
                    items.forEach((item, index) => {
                        setTimeout(() => {
                            item.classList.add('visible');
                        }, index * 200);
                    });
                }
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    });
    
    // Observe all elements with fade-in class
    document.querySelectorAll('.fade-in').forEach(el => {
        observer.observe(el);
    });
    
    // Team card hover effects
    const teamCards = document.querySelectorAll('.team-card');
    teamCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-10px)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0)';
        });
    });
    
    // Value card hover effects
    const valueCards = document.querySelectorAll('.value-card');
    valueCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-5px)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0)';
        });
    });
    
    // Mission card hover effects
    const missionCards = document.querySelectorAll('.mission-card');
    missionCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-10px)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0)';
        });
    });
    
    // Smooth scroll for navigation
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const href = this.getAttribute('href');
            if (href !== '#' && href.startsWith('#')) {
                e.preventDefault();
                const target = document.querySelector(href);
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            }
        });
    });
    
    // Animate impact stats
    const impactStats = document.querySelectorAll('.impact-stat .stat-number');
    if (impactStats.length > 0) {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    impactStats.forEach(stat => {
                        const text = stat.textContent;
                        const number = parseInt(text.replace(/[^0-9]/g, ''));
                        const suffix = text.replace(number.toString(), '');
                        
                        stat.textContent = '0' + suffix;
                        
                        if (number > 0) {
                            let count = 0;
                            const duration = 1500;
                            const increment = number / (duration / 16);
                            
                            const counter = setInterval(() => {
                                count += increment;
                                if (count >= number) {
                                    count = number;
                                    clearInterval(counter);
                                }
                                stat.textContent = Math.round(count) + suffix;
                            }, 16);
                        }
                    });
                }
            });
        }, { threshold: 0.5 });
        
        observer.observe(document.querySelector('.impact-section'));
    }
    
    // Add parallax effect to hero
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;
        const hero = document.querySelector('.about-hero');
        if (hero) {
            hero.style.transform = `translateY(${scrolled * 0.5}px)`;
        }
    });
    
    // Add floating ingredient animations
    const ingredients = document.querySelectorAll('.floating-ingredient');
    ingredients.forEach((ingredient, index) => {
        ingredient.style.animationDelay = `${index * 2}s`;
    });
});
